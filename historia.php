	<div id="hist_text"><p class="parrafo" >El projecte de La Juguesca arrenca l’any 1995 quan un grup de joves de l’Aliança de Lliçà d’Amunt es decideix a crear una “festa major alternativa” per demostrar que les coses es poden fer d’una altra manera, primant la participació de la gent i combinant l’aspecte esportiu amb el rural. <br>
Aquest grup de gent es va dedicar a preparar un seguit de proves per a què la gent del poble hi participi des de diferents colles. El número de colles ha anat variant durant els primers anys passant de les 3 colles inicials fins les cinc que hi ha actualment. L’any 1999 es va decidir no acceptar cap colla més i que si algun grup volia participar a la Juguesca el que havia de fer era incorporar-se a qualsevol de les cinc colles ja existents i així reforçar-les amb un nombre més elevat de participants. <br>
Evolució de les colles participants:</p>
<ul>
<li>	1995: Concos, Encarnación, Sporting l’Olla</li>
<li>	1996: Can t’implora, Concos (junt amb Encarnación), Sporting l’Olla</li>
<li>	1997: Can t’implora, Concos, Sporting l’Olla, Skando</li>
<li>	1998 - 2012: Bisky, Can t’implora, Concos, Sporting l’Olla, Skando</li>
</ul>

A l’inici cada colla representava un sector de la població del poble:<br>
<ul>
<li>	Concos: Tal i com diu el seu nom, eren un grup de solters amb una edat rondant la trentena i a més a més eren bons esportistes (la gran majoria).</li>
<li>	Encarnación: Podríem dir que en aquesta colla es van agrupar els joves “rebels” que en el seu moment s’havien encarregat de portar la discoteca de l’Aliança. A la Juguesca només van durar 2 anys, el primer en solitari i el segon junts amb Concos, el tercer any ja es van dissoldre totalment dins la colla de Concos.</li>
<li>	Sporting l’Olla: En aquesta colla s’hi agrupa un seguit de persones que com diu el nom de la colla no estan gaire bé de l’olla, sorgeixen al muntar un equip de futbol en els anys 80 i durant els seus anys d’existència (o sigui, fins a dia d’avui encara) van creant o muntant diferents esdeveniments pel poble.</li>
<li>	Can t’implora: La gent que estava relacionada amb l’esplai va crear aquesta colla.</li>
<li>	Skando: Els joves de Can t’implora es van separar de la colla i van crear-ne una de nova per poder tenir més participació dins de la Juguesca.</li>
<li>	Bisky: Un grup de nois de Can Farell creen aquesta colla i durant els primers anys seria la colla representativa de les urbanitzacions del poble, la gent que no estava tan relacionada històricament amb les activitats que es realitzaven a Lliçà.</li>
</ul>
<p class="parrafo">
Durant aquests anys les proves han anat variant però sempre s'han mantingut l'esperit inicial. Cada any es desenvolupen proves totalment diferents a les edicions anteriors, per tant els participants no saben què hauran de fer fins que arriben a la prova! <br>
Només hi ha una de les proves que es manté quasi invariable. la famosa i més espectacular ESTIRADA DEL TRACTOR de Lliçà d'Amunt! <br>
Expliquem-ho: L’estirada del tractor de la Juguesca, com el seu nom diu és literalment l'estirada d'un tractor al <a class="tex" href="https://www.google.es/maps/@41.6108173,2.2401107,3a,75y,271.46h,87.77t/data=!3m4!1e1!3m2!1sJk3r152_Rsyo4oYPgJ7K2Q!2e0!6m1!1e1" >carrer de l'Aliança</a> de Lliçà, un tractor amb remolc (pes total, 14 Tones aprox), un carrer amb pujada, una corda, una colla i el temps que triguen en pujar-lo 100m carrer amunt! això és aquesta prova! Senzillament espectacular!<br>
